void main() {
  print('Hello World');

  //deklarasi menggunakan tipe data
  String nama = "Trisna"; //tipe data string
  int umur = 22; //tipe data integer
  print(nama);
  print(umur);

  //deklarasi menggunakan var
  var name = "John";
  print(name);

  //deklarasi menggunakan final
  final lastName = "Doe";
  print(lastName);

  //deklarasi menggunakan const
  const PI = 3.14;
  print(PI);

  //deklarasi menggunakan let
  late String address;
  address = 'Jl. ABC';
  print(address);
}
